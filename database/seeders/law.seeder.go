func LawSeeder() {
	names := generateRandomNames(1000)
	texts := generateTexts(1000)
	titles := generateTitles(1000)
	for i := 0; i < 1000; i++ {
		rand.Seed(time.Now().UnixNano()) 
		D.DB().Create(&M.Law{
			Type:               rand.Intn(3) + 1,
			Title:              titles[i],
			SessionNumber:      11,
			SessionDate:        time.Date(1989, 4, 12, 0, 0, 0, 0, time.UTC),
			NotificationNumber: "120/4720",
			NotificationDate:   time.Date(1989, 8, 3, 0, 0, 0, 0, time.UTC),
			Body:               texts[i],
			Image:              "logo-website.png",
			NumberItems:        rand.Intn(20) + 1,
			NumberNotes:        rand.Intn(20) + 1,
			Recommender:        names[i],
		})
	}

}
