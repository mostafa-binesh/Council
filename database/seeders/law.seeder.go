func generateRandomNames(n int) []string {
	var result []string
	for i := 0; i < n; i++ {
		firstName := firstNames[rand.Intn(len(firstNames))]
		lastName := lastNames[rand.Intn(len(lastNames))]
		fullName := fmt.Sprintf("%s %s", firstName, lastName)
		result = append(result, fullName)
	}
	return result
}

